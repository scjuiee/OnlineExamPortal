package org.accenture.oep.bean;

public class CandidateScoreProfile {
	
	public String userAnswers;
	public String userScore;
	public String empId;
	public String examVoucher;
	public String name;
	
	public String getUserAnswers() {
		return userAnswers;
	}
	public void setUserAnswers(String userAnswers) {
		this.userAnswers = userAnswers;
	}
	public String getUserScore() {
		return userScore;
	}
	public void setUserScore(String userScore) {
		this.userScore = userScore;
	}
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getExamVoucher() {
		return examVoucher;
	}
	public void setExamVoucher(String examVoucher) {
		this.examVoucher = examVoucher;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
